package CustomClassPackage;

public class Segment {
    Double x;
    Double z1;
    Double z2;

    public Segment(Double x, Double z1, Double z2) {
        this.x = x;
        this.z1 = z1;
        this.z2 = z2;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getZ1() {
        return z1;
    }

    public void setZ1(Double z1) {
        this.z1 = z1;
    }

    public Double getZ2() {
        return z2;
    }

    public void setZ2(Double z2) {
        this.z2 = z2;
    }
}
